public class PracticeIfStatement
{
   public static void main(String[] args)
   {
      int num = 3456;
      
      if (num % 2 == 0) //even number would be true
      {
         System.out.println("Your number is even");
      }
      
      if (num % 3 == 0)
      {
         System.out.println("Your number is divisible by 3");
      }
   }
}
      
      
      
